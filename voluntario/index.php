					  <h4 class="timeline-title">Cada um é voluntário a seu modo</h4>
					  <p>Alguns são capazes individualmente de identificar um problema, arregaçar as mangas e agir. Outros preferem atuar em grupo. Grupos de vizinhos, de amigos, de estudantes ou aposentados, de colegas de trabalho que se mobilizam para ajudar pessoas e comunidades. Por vezes, é uma instituição inteira que se mobiliza, seja ela um clube, uma igreja, uma entidade beneficente ou uma empresa. No voluntariado é assim: não há fórmulas nem receitas a serem seguidas.</p>
