					  <p>Voluntariado não tem nada a ver com obrigação, com coisa chata, triste, motivada por sentimento de culpa. Voluntariado é uma experiência espontânea, alegre, prazerosa, gratificante. O voluntário doa sua energia, tempo e talento mas ganha muitas coisas em troca: contato humano, convivência com pessoas diferentes, oportunidade de viver outras situações, aprender coisas novas, satisfação de se sentir útil.</p>
